def build_autocomplete_prompt(category: str, tool: str, hint: str, ai_mode: str) -> str:
    """
    ユーザーのヒント（一言説明）から各フォーム入力項目の記入案を生成するプロンプト。
    返り値はプロンプト文字列。ユーザーはこれを ChatGPT 等に貼り付けて使う。
    """
    cat_map = _FIELD_MAPS.get(category, {})
    tool_map = cat_map.get(tool, {})
    tool_label = tool_map.get("label", "副業支援ツール")
    fields = tool_map.get("fields", [("テーマ", ""), ("ターゲット", ""), ("目的", "")])

    fields_block = "\n".join(
        f"  ・{name}（{desc}）" if desc else f"  ・{name}"
        for name, desc in fields
    )

    return f"""あなたは副業コンサルタントとコピーライターを兼ねた専門家です。
ユーザーが副業支援ツール「{tool_label}」を使うために必要な入力情報を、
簡単なヒントから具体的かつコピーしてすぐ使えるレベルで提案してください。

【ユーザーが作りたいもの（ヒント）】
{hint}

【提案が必要なフォーム項目】
{fields_block}

上記のフォーム項目それぞれについて、ヒントを元に具体的な入力案を提案してください。

出力フォーマット（この形式を厳守）:
【項目名】
→ 提案内容

注意事項:
・抽象的な表現は避け、具体的な数字・言葉を使う
・ユーザーがそのままコピー&ペーストして使える内容にする
・ターゲット像は年齢・状況・悩みまで詳しく具体化する
・「〇〇など」「〇〇等」という曖昧な表現より、断言した表現を使う{ai_suffix(ai_mode)}""".strip()


