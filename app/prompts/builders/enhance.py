def build_image_prompt(p) -> str:
    """
    記事テーマから画像生成 AI 向けプロンプトを生成するプロンプトを作成する。
    p は ImagePromptRequest インスタンス。
    """
    image_desc = _IMAGE_TYPE_MAP.get(p.image_type, "汎用コンテンツ用画像")
    platform_note = _PLATFORM_NOTES.get(p.platform, "英語プロンプトを基本とする。")

    return f"""あなたは画像生成 AI の専門プロンプトエンジニアです。
副業コンテンツの画像として最適なプロンプトを設計するプロとして、
以下の条件で高品質な画像生成プロンプトを作成してください。

【記事テーマ・コンテンツ概要】
{p.theme}

【画像の用途】
{image_desc}

【スタイル・雰囲気の方向性】
{p.style}

【対象プラットフォーム】
{p.platform}
補足: {platform_note}

【作成内容】

■ メイン画像プロンプト（3案）
同じテーマを異なるビジュアルアプローチで 3 案作成してください:

案1: 抽象・コンセプト系
  テーマを概念的・象徴的に表現する。人物なし・アイコニックなビジュアル。

案2: 人物・共感系
  ターゲット読者が「これは自分だ」と感じるリアルなシーン描写。

案3: グラフィック・情報系
  データ・数字・矢印などを活用したインフォグラフィック的ビジュアル。

各案に必ず含めるもの:
  ・{p.platform} 向け完成プロンプト（コピーしてすぐ使えるもの）
  ・除外指定（避けるべき要素・negative プロンプト）
  ・推奨パラメータ・設定値

■ カラーバリエーション提案
各案について、色彩アプローチの違い（2 パターン）を提案してください:
  ・ダーク系（高級感・信頼感）
  ・ライト系（親しみやすさ・明るさ）

■ {p.platform} 活用のコツ（2〜3 点）
最高品質を引き出すための設定・ヒント{ai_suffix(p.ai_mode)}""".strip()
