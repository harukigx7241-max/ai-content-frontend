"""
app/prompts/modifiers/ai_optimize.py
AI プラン別深層最適化モディファイア。
Phase 0 の ai_suffix() が「補足ヒント」に留まるのに対し、
こちらは各 AI の能力・仕様に踏み込んだ具体的な指示を追加する。
TODO: Phase 3+ で AI ごとのカスタム指示を設定から管理できるようにする。
"""

_CHATGPT = """\
【ChatGPT 深層最適化指示】
・思考の連鎖（Chain of Thought）で段階的に考え、最終回答を出力してください
・Markdown 記法（## 見出し / **太字** / - 箇条書き / > 引用）を積極的に使い構造化してください
・数字・統計を引用する際は「（出典: 〇〇）」形式で明記してください
・1,500 字を超える場合、末尾に TLDR（3 行以内の要約）を追記してください
・GPT-4 の長文処理能力を活かし、各セクションのつながりを意識した一貫性のある文章にしてください"""

_GEMINI = """\
【Gemini 深層最適化指示】
・Google 検索を積極的に使い、「2025 年最新データによると...」など時制を明確にして引用してください
・検索結果はそのままコピーせず、文脈に合わせて解釈・要約して盛り込んでください
・Gemini のマルチモーダル能力を活かし、図表・グラフ・インフォグラフィックの提案も含めてください
・ファクトチェックを経た上で断定的な表現を使い、信頼性を高めてください
・日本語ページより英語ページの方が最新情報が多い場合は、英語ソースも参照して翻訳・要約してください"""

_CLAUDE = """\
【Claude 深層最適化指示】
・<thinking> タグ内で事前に構造・論理・品質を検討してから本文を出力してください
・最終的な本文は <output> タグで囲み、他の内容と明確に分けてください
・セクションは <section name="〇〇"> タグで囲み、重要箇所は <key_point> タグを活用してください
・出力末尾に <quality_check>（自己評価：網羅性 / 具体性 / 読みやすさの 3 点）を追加してください
・Claude の長文コンテキスト能力を活かし、全体の一貫性と論理構造を最優先にしてください"""

_OPTIMIZE_MAP: dict[str, str] = {
    "ChatGPT": _CHATGPT,
    "Gemini": _GEMINI,
    "Claude": _CLAUDE,
}


def apply_ai_optimize(prompt: str, ai_mode: str) -> str:
    """AI モードに応じた深い最適化指示をプロンプトに追加する。"""
    instruction = _OPTIMIZE_MAP.get(ai_mode, _CHATGPT)
    return f"{prompt}\n\n{instruction}"
