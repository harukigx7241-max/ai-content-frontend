"""
app/prompts/modifiers/direct_output.py
「前置き不要・本文直接出力」モディファイア。
既存プロンプトの末尾に追加するだけで機能するシンプルな設計。
TODO: Phase 3+ で出力フォーマット別の細かいルールを追加できる。
"""

_INSTRUCTION = """\
【重要: 出力ルール】
・「はい」「分かりました」「以下に〜」「作成します」等の前置きは一切不要です
・最初の一文から直接本文を書き始めてください
・確認文・補足説明・感想・「いかがでしたか？」等の後書きも不要です
・指定された形式の本文のみをそのまま出力してください"""


def apply_direct_output(prompt: str) -> str:
    """プロンプトに「前置き不要・本文直接出力」の指示を追加する。"""
    return f"{prompt}\n\n{_INSTRUCTION}"
